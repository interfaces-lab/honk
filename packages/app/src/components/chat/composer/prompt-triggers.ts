import { splitPromptIntoComposerSegments, type ComposerPromptSegment } from "./prompt-segments";

export type ComposerTriggerKind = "path" | "slash-command";
export type ComposerSlashCommand = "model" | "plan" | "default";

export interface ComposerTrigger {
  kind: ComposerTriggerKind;
  query: string;
  rangeStart: number;
  rangeEnd: number;
}

const isInlineTokenSegment = (segment: ComposerPromptSegment): boolean => segment.type !== "text";

function clampCursor(text: string, cursor: number): number {
  if (!Number.isFinite(cursor)) return text.length;
  return Math.max(0, Math.min(text.length, Math.floor(cursor)));
}

function isWhitespace(char: string): boolean {
  return char === " " || char === "\n" || char === "\t" || char === "\r";
}

const SLASH_COMMAND_LOOKBEHIND_LIMIT = 200;
const SLASH_COMMAND_MAX_QUERY_SEGMENTS = 120;
const SLASH_COMMAND_MAX_SIMPLE_QUERY_LENGTH = 50;
const SLASH_COMMAND_SPECIAL_CHARS = String.raw`,\+\*\?\$\@\|#{}\(\)\^\-\[\]\\!%'"~=<>:;`;
const SLASH_COMMAND_QUERY_CHAR = String.raw`[^/${SLASH_COMMAND_SPECIAL_CHARS}\s]`;
const SLASH_COMMAND_QUERY_SEPARATOR = String.raw`(?:\.[ |$]| |[${SLASH_COMMAND_SPECIAL_CHARS}/]|)`;
const SLASH_COMMAND_QUERY_PATTERN = new RegExp(
  String.raw`(^|\s|\()([/]((?:${SLASH_COMMAND_QUERY_CHAR}${SLASH_COMMAND_QUERY_SEPARATOR}){0,${SLASH_COMMAND_MAX_QUERY_SEGMENTS}}))$`,
);
const SLASH_COMMAND_SIMPLE_QUERY_PATTERN = new RegExp(
  String.raw`(^|\s|\()([/]((?:${SLASH_COMMAND_QUERY_CHAR}){0,${SLASH_COMMAND_MAX_SIMPLE_QUERY_LENGTH}}))$`,
);

function recognizeStandaloneSlashCommand(textBeforeCursor: string): {
  query: string;
  rangeStart: number;
} | null {
  const windowStart = Math.max(0, textBeforeCursor.length - SLASH_COMMAND_LOOKBEHIND_LIMIT);
  const textWindow = textBeforeCursor.slice(windowStart);
  const match =
    SLASH_COMMAND_QUERY_PATTERN.exec(textWindow) ??
    SLASH_COMMAND_SIMPLE_QUERY_PATTERN.exec(textWindow);
  if (!match) return null;

  const lead = match[1] ?? "";
  return {
    query: match[3] ?? "",
    rangeStart: windowStart + match.index + lead.length,
  };
}

function tokenStartForCursor(text: string, cursor: number): number {
  let index = cursor - 1;
  while (index >= 0 && !isWhitespace(text[index] ?? "")) {
    index -= 1;
  }
  return index + 1;
}

function expandedSkillSegmentLength(segment: { type: "skill"; name: string; path?: string }) {
  return segment.path ? `[$${segment.name}](${segment.path})`.length : segment.name.length + 1;
}

export function expandCollapsedComposerCursor(text: string, cursorInput: number): number {
  const collapsedCursor = clampCursor(text, cursorInput);
  const segments = splitPromptIntoComposerSegments(text);
  if (segments.length === 0) {
    return collapsedCursor;
  }

  let remaining = collapsedCursor;
  let expandedCursor = 0;

  for (const segment of segments) {
    if (segment.type === "mention") {
      const expandedLength = segment.path.length + 1;
      if (remaining <= 1) {
        return expandedCursor + (remaining === 0 ? 0 : expandedLength);
      }
      remaining -= 1;
      expandedCursor += expandedLength;
      continue;
    }
    if (segment.type === "skill") {
      const expandedLength = expandedSkillSegmentLength(segment);
      if (remaining <= 1) {
        return expandedCursor + (remaining === 0 ? 0 : expandedLength);
      }
      remaining -= 1;
      expandedCursor += expandedLength;
      continue;
    }
    if (segment.type === "inline-token") {
      const expandedLength = segment.markdown.length;
      if (remaining <= 1) {
        return expandedCursor + (remaining === 0 ? 0 : expandedLength);
      }
      remaining -= 1;
      expandedCursor += expandedLength;
      continue;
    }

    const segmentLength = segment.text.length;
    if (remaining <= segmentLength) {
      return expandedCursor + remaining;
    }
    remaining -= segmentLength;
    expandedCursor += segmentLength;
  }

  return expandedCursor;
}

function collapsedSegmentLength(segment: ComposerPromptSegment): number {
  if (segment.type === "text") {
    return segment.text.length;
  }
  return 1;
}

function clampCollapsedComposerCursorForSegments(
  segments: ReadonlyArray<ComposerPromptSegment>,
  cursorInput: number,
): number {
  const collapsedLength = segments.reduce(
    (total, segment) => total + collapsedSegmentLength(segment),
    0,
  );
  if (!Number.isFinite(cursorInput)) {
    return collapsedLength;
  }
  return Math.max(0, Math.min(collapsedLength, Math.floor(cursorInput)));
}

export function clampCollapsedComposerCursor(text: string, cursorInput: number): number {
  return clampCollapsedComposerCursorForSegments(
    splitPromptIntoComposerSegments(text),
    cursorInput,
  );
}

export function collapseExpandedComposerCursor(text: string, cursorInput: number): number {
  const expandedCursor = clampCursor(text, cursorInput);
  const segments = splitPromptIntoComposerSegments(text);
  if (segments.length === 0) {
    return expandedCursor;
  }

  let remaining = expandedCursor;
  let collapsedCursor = 0;

  for (const segment of segments) {
    if (segment.type === "mention") {
      const expandedLength = segment.path.length + 1;
      if (remaining === 0) {
        return collapsedCursor;
      }
      if (remaining <= expandedLength) {
        return collapsedCursor + 1;
      }
      remaining -= expandedLength;
      collapsedCursor += 1;
      continue;
    }
    if (segment.type === "skill") {
      const expandedLength = expandedSkillSegmentLength(segment);
      if (remaining === 0) {
        return collapsedCursor;
      }
      if (remaining <= expandedLength) {
        return collapsedCursor + 1;
      }
      remaining -= expandedLength;
      collapsedCursor += 1;
      continue;
    }
    if (segment.type === "inline-token") {
      const expandedLength = segment.markdown.length;
      if (remaining === 0) {
        return collapsedCursor;
      }
      if (remaining <= expandedLength) {
        return collapsedCursor + 1;
      }
      remaining -= expandedLength;
      collapsedCursor += 1;
      continue;
    }

    const segmentLength = segment.text.length;
    if (remaining <= segmentLength) {
      return collapsedCursor + remaining;
    }
    remaining -= segmentLength;
    collapsedCursor += segmentLength;
  }

  return collapsedCursor;
}

export function isCollapsedCursorAdjacentToInlineToken(
  text: string,
  cursorInput: number,
  direction: "left" | "right",
): boolean {
  const segments = splitPromptIntoComposerSegments(text);
  if (!segments.some(isInlineTokenSegment)) {
    return false;
  }

  const cursor = clampCollapsedComposerCursorForSegments(segments, cursorInput);
  let collapsedOffset = 0;

  for (const segment of segments) {
    if (isInlineTokenSegment(segment)) {
      if (direction === "left" && cursor === collapsedOffset + 1) {
        return true;
      }
      if (direction === "right" && cursor === collapsedOffset) {
        return true;
      }
    }
    collapsedOffset += collapsedSegmentLength(segment);
  }

  return false;
}

export const isCollapsedCursorAdjacentToMention = isCollapsedCursorAdjacentToInlineToken;

export function detectComposerTrigger(text: string, cursorInput: number): ComposerTrigger | null {
  const cursor = clampCursor(text, cursorInput);
  const slashCommand = recognizeStandaloneSlashCommand(text.slice(0, cursor));

  if (slashCommand) {
    return {
      kind: "slash-command",
      query: slashCommand.query,
      rangeStart: slashCommand.rangeStart,
      rangeEnd: cursor,
    };
  }

  const tokenStart = tokenStartForCursor(text, cursor);
  const token = text.slice(tokenStart, cursor);
  if (!token.startsWith("@")) {
    return null;
  }

  return {
    kind: "path",
    query: token.slice(1),
    rangeStart: tokenStart,
    rangeEnd: cursor,
  };
}

export function parseStandaloneComposerSlashCommand(
  text: string,
): Exclude<ComposerSlashCommand, "model"> | null {
  const match = /^\/(plan|default)\s*$/i.exec(text.trim());
  if (!match) {
    return null;
  }
  const command = match[1]?.toLowerCase();
  if (command === "plan") return "plan";
  return "default";
}

export function replaceTextRange(
  text: string,
  rangeStart: number,
  rangeEnd: number,
  replacement: string,
): { text: string; cursor: number } {
  const safeStart = Math.max(0, Math.min(text.length, rangeStart));
  const safeEnd = Math.max(safeStart, Math.min(text.length, rangeEnd));
  const nextText = `${text.slice(0, safeStart)}${replacement}${text.slice(safeEnd)}`;
  return { text: nextText, cursor: safeStart + replacement.length };
}
