import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const pluginRoot = resolve(packageRoot, "src/host-plugin");
const outputPath = resolve(packageRoot, "src/host-plugin-manifest.generated.ts");
const moduleNames = [
  "index.ts",
  "agents.ts",
  "pairing.ts",
  "plan-submit.ts",
  "types.ts",
];

const modules = await Promise.all(
  moduleNames.map(async (fileName) => ({
    fileName,
    source: await readFile(resolve(pluginRoot, fileName), "utf8"),
  })),
);
const generated = `// Generated by scripts/generate-host-plugin-manifest.mjs. Do not edit.\nexport const HONK_OPENCODE_PLUGIN_MODULES = ${JSON.stringify(modules, null, 2)} as const;\n`;

if (process.argv.includes("--check")) {
  const current = await readFile(outputPath, "utf8").catch(() => "");
  if (current !== generated) {
    process.stderr.write(
      "The Honk OpenCode plugin manifest is stale. Run pnpm --filter @honk/opencode generate:host-plugin.\n",
    );
    process.exitCode = 1;
  }
} else {
  await writeFile(outputPath, generated);
}
