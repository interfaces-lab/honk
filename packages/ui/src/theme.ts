export type ThemeMode = keyof typeof honkTheme.colors;
export type NativeTheme = {
  readonly mode: ThemeMode;
  readonly colors: ThemeColors;
  readonly metrics: typeof nativeMetrics;
};

export const resolveNativeTheme = (mode: ThemeMode): NativeTheme => ({
  mode,
  colors: honkTheme.colors[mode],
  metrics: honkTheme.native,
});
